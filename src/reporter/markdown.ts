export function generateMarkdownReport(
  flow: AuthFlow,
  cookieDiff: CookieDiff,
  storageDiff: StorageDiff,
  opts: MarkdownOptions = {},
  strings: ReportStrings = DEFAULT_REPORT_STRINGS,
): string {
  const s = strings;
  const out: string[] = [];
  out.push(`# ${s.title}`);
  out.push('');
  out.push(`> ${s.disclaimer}`);
  out.push('');

  // Summary
  out.push(`## ${s.summaryHeading}`);
  out.push('');
  out.push(`- **${s.labelTarget}:** ${flow.targetUrl}`);
  out.push(`- **${s.labelStarted}:** ${flow.startedAt}`);
  if (flow.endedAt) out.push(`- **${s.labelEnded}:** ${flow.endedAt}`);
  out.push(`- **${s.labelRequestsCaptured}:** ${flow.requests.length}`);
  out.push(`- **${s.labelLoginCandidates}:** ${flow.loginCandidates.length}`);
  out.push('');

  // Auth Type
  out.push(`## ${s.authTypeHeading}`);
  out.push('');
  if (flow.summary) {
    out.push(
      `- **${s.labelAuthType}:** \`${flow.summary.authType}\` (${s.formatConfidence(flow.summary.confidence.toFixed(0), flow.summary.confidenceLevel)})`,
    );
    if (flow.summary.detectedSignals.length > 0) {
      out.push('');
      out.push(`**${s.signalsLabel}**`);
      out.push('');
      for (const sig of flow.summary.detectedSignals) {
        out.push(`- [+${sig.weight}] ${sig.description}`);
      }
    }
  } else {
    out.push(s.noSummary);
  }
  out.push('');

  // Login Candidate
  out.push(`## ${s.loginCandidateHeading}`);
  out.push('');
  const top = flow.loginCandidates[0];
  if (top) {
    const req = flow.requests.find((r) => r.id === top.requestId);
    out.push(`- **${s.labelScore}:** ${top.score} (${top.confidence})`);
    if (req) {
      out.push(`- **${s.labelMethodUrl}:** \`${req.method.toUpperCase()} ${req.url}\``);
    }
    out.push('');
    out.push(`**${s.reasonsLabel}**`);
    out.push('');
    for (const r of top.reasons) {
      out.push(`- ${r}`);
    }
  } else {
    out.push(s.noLoginCandidate1);
    out.push(s.noLoginCandidate2);
  }
  out.push('');

  // Cookie Changes
  out.push(`## ${s.cookieHeading}`);
  out.push('');
  if (
    cookieDiff.added.length === 0 &&
    cookieDiff.removed.length === 0 &&
    cookieDiff.changed.length === 0
  ) {
    out.push(s.noCookieChanges);
  } else {
    if (cookieDiff.added.length > 0) {
      out.push(`**${s.addedLabel}**`);
      out.push('');
      for (const c of cookieDiff.added) {
        out.push(
          `- \`${c.name}\` @ ${c.domain}${c.path} → ${cookieValueDisplay(c.value, opts)} ${flagSuffix(c)}`,
        );
      }
      out.push('');
    }
    if (cookieDiff.changed.length > 0) {
      out.push(`**${s.changedLabel}**`);
      out.push('');
      for (const c of cookieDiff.changed) {
        out.push(
          `- \`${c.after.name}\` @ ${c.after.domain}${c.after.path}: ${cookieValueDisplay(c.before.value, opts)} → ${cookieValueDisplay(c.after.value, opts)}`,
        );
      }
      out.push('');
    }
    if (cookieDiff.removed.length > 0) {
      out.push(`**${s.removedLabel}**`);
      out.push('');
      for (const c of cookieDiff.removed) {
        out.push(`- \`${c.name}\` @ ${c.domain}${c.path}`);
      }
      out.push('');
    }
  }
  out.push('');

  // Storage Changes
  out.push(`## ${s.storageHeading}`);
  out.push('');
  appendStorageSection(out, 'localStorage', storageDiff.localStorage, opts, s);
  appendStorageSection(out, 'sessionStorage', storageDiff.sessionStorage, opts, s);
  out.push('');

  // Redirect Flow
  out.push(`## ${s.redirectHeading}`);
  out.push('');
  if (flow.redirects.length === 0) {
    out.push(`_${s.noRedirects}_`);
  } else {
    for (const r of flow.redirects) {
      out.push(`- ${r.status}: ${r.fromUrl} → ${r.toUrl}`);
    }
  }
  out.push('');

  // Mermaid Diagram
  out.push(`## ${s.diagramHeading}`);
  out.push('');
  out.push('```mermaid');
  out.push(generateMermaidDiagram(flow));
  out.push('```');
  out.push('');

  // Timeline (compact by default — only noteworthy auth events).
  out.push(`## ${s.timelineHeading}`);
  out.push('');
  const compact = opts.compactTimeline !== false;
  const shownSteps = compact
    ? flow.steps.filter((step) => isNoteworthyEvent(step.event as AuthEvent, step.index))
    : flow.steps;
  if (shownSteps.length === 0) {
    out.push(`_${s.noTimeline}_`);
  } else {
    let n = 0;
    for (const step of shownSteps) {
      n += 1;
      out.push(`${n}. **${step.event.type}** — ${step.description}`);
    }
    if (compact && flow.steps.length > shownSteps.length) {
      out.push('');
      out.push(`_+${flow.steps.length - shownSteps.length} additional events omitted._`);
    }
  }
  out.push('');

  // OAuth / OIDC — only render if authorize or token endpoint observed.
  const oauth = findOAuthFlow(flow);
  if (oauth.authorizeRequests.length > 0 || oauth.tokenExchanges.length > 0) {
    out.push(`## ${s.oauthHeading}`);
    out.push('');
    for (const a of oauth.authorizeRequests) {
      out.push(`### ${s.oauthAuthorizeHeading} — \`${a.endpoint}\``);
      out.push('');
      if (a.responseType) out.push(`- **${s.oauthResponseType}:** \`${a.responseType}\``);
      if (a.clientId) out.push(`- **${s.oauthClientId}:** \`${a.clientId}\``);
      if (a.redirectUri) out.push(`- **${s.oauthRedirectUri}:** \`${a.redirectUri}\``);
      if (a.scope) out.push(`- **${s.oauthScopeRequested}:** \`${a.scope}\``);
      if (a.state) out.push(`- **${s.oauthState}:** \`${a.state}\``);
      if (a.nonce) out.push(`- **${s.oauthNonce}:** \`${a.nonce}\``);
      out.push(
        `- **${s.oauthPkce}:** ${a.pkce ? s.oauthPkceYes : s.oauthPkceNo}${a.codeChallengeMethod ? ` (\`${a.codeChallengeMethod}\`)` : ''}`,
      );
      out.push('');
    }
    for (const e of oauth.tokenExchanges) {
      out.push(`### ${s.oauthTokenHeading} — \`${e.endpoint}\``);
      out.push('');
      if (e.grantType) out.push(`- **${s.oauthGrantType}:** \`${e.grantType}\``);
      if (e.clientId) out.push(`- **${s.oauthClientId}:** \`${e.clientId}\``);
      if (e.tokenType) out.push(`- **${s.oauthTokenType}:** \`${e.tokenType}\``);
      if (e.scope) out.push(`- **${s.oauthScopeGranted}:** \`${e.scope}\``);
      if (e.expiresInSeconds !== undefined) {
        out.push(`- **${s.oauthExpiresIn}:** \`${e.expiresInSeconds}s\``);
      }
      if (e.expiresAt) {
        out.push(`- **${s.oauthExpiresAt}:** ${e.expiresAt.toISOString()}`);
      }
      out.push(`- **${s.oauthRefreshToken}:** ${e.hasRefreshToken ? s.oauthYes : s.oauthNo}`);
      out.push(`- **${s.oauthIdToken}:** ${e.hasIdToken ? s.oauthYes : s.oauthNo}`);
      out.push('');
    }
  }

  // Decoded JWTs — only render section if any tokens were found.
  const jwts = findJwts(flow);
  if (jwts.length > 0) {
    out.push(`## ${s.jwtHeading}`);
    out.push('');
    for (const j of jwts) {
      out.push(`### ${s.jwtSourceLabels[j.source]} — \`${j.label}\``);
      out.push('');
      const claims: string[] = [];
      if (j.decoded.algorithm) claims.push(`- **${s.jwtAlgorithm}:** \`${j.decoded.algorithm}\``);
      if (typeof j.decoded.payload.sub === 'string')
        claims.push(`- **${s.jwtSubject}:** \`${j.decoded.payload.sub}\``);
      if (typeof j.decoded.payload.iss === 'string')
        claims.push(`- **${s.jwtIssuer}:** \`${j.decoded.payload.iss}\``);
      if (j.decoded.payload.aud !== undefined)
        claims.push(`- **${s.jwtAudience}:** \`${JSON.stringify(j.decoded.payload.aud)}\``);
      if (j.decoded.issuedAt)
        claims.push(`- **${s.jwtIssuedAt}:** ${j.decoded.issuedAt.toISOString()}`);
      if (j.decoded.expiresAt) {
        const status = j.decoded.expired ? s.jwtExpired : s.jwtNotExpired;
        claims.push(
          `- **${s.jwtExpiresAt}:** ${j.decoded.expiresAt.toISOString()} _(${status})_`,
        );
      }
      claims.push(`- **${s.jwtSignaturePreview}:** \`${j.decoded.signaturePreview}\``);
      for (const line of claims) out.push(line);
      out.push('');
      out.push(`**${s.jwtHeader}:**`);
      out.push('');
      out.push('```json');
      out.push(JSON.stringify(j.decoded.header, null, 2));
      out.push('```');
      out.push('');
      out.push(`**${s.jwtPayload}:**`);
      out.push('');
      out.push('```json');
      out.push(JSON.stringify(j.decoded.payload, null, 2));
      out.push('```');
      out.push('');
    }
  }

  // Security Notes
  out.push(`## ${s.securityHeading}`);
  out.push('');
  if (flow.summary && flow.summary.warnings.length > 0) {
    for (const w of flow.summary.warnings) {
      out.push(`- _${w.level}_: ${w.message}`);
    }
  } else {
    out.push(s.noSecurityNotes);
  }
  out.push('');
  out.push(`> ${s.footer}`);
  out.push('');

  return out.join('\n');
}

function cookieValueDisplay(
  value: { masked: string; raw?: string },
  opts: MarkdownOptions,
): string {
  if (opts.includeRaw && !opts.enforceMasking && value.raw) {
    return '`' + value.raw + '`';
  }
  return '`' + value.masked + '`';
}

function flagSuffix(c: {
  httpOnly: boolean;
  secure: boolean;
  sameSite?: string;
}): string {
  const flags: string[] = [];
  if (c.httpOnly) flags.push('HttpOnly');
  if (c.secure) flags.push('Secure');
  if (c.sameSite) flags.push(`SameSite=${c.sameSite}`);
  return flags.length > 0 ? `_(${flags.join(', ')})_` : '';
}

function appendStorageSection(
  out: string[],
  title: string,
  diff: StorageDiff['localStorage'],
  opts: MarkdownOptions,
  s: ReportStrings,
) {
  out.push(`### ${title}`);
  out.push('');
  if (diff.added.length === 0 && diff.changed.length === 0 && diff.removed.length === 0) {
    out.push(`_${s.noStorageChanges}_`);
    out.push('');
    return;
  }
  if (diff.added.length > 0) {
    out.push(`**${s.addedLabel}**`);
    for (const e of diff.added) {
      out.push(`- \`${e.key}\` = \`${displayValue(e.value, opts)}\``);
    }
    out.push('');
  }
  if (diff.changed.length > 0) {
    out.push(`**${s.changedLabel}**`);
    for (const e of diff.changed) {
      out.push(`- \`${e.after.key}\``);
    }
    out.push('');
  }
  if (diff.removed.length > 0) {
    out.push(`**${s.removedLabel}**`);
    for (const e of diff.removed) {
      out.push(`- \`${e.key}\``);
    }
    out.push('');
  }
}

function displayValue(
  value: { masked: string; raw?: string },
  opts: MarkdownOptions,
): string {
  if (opts.includeRaw && !opts.enforceMasking && value.raw) return value.raw;
  return value.masked;
}
