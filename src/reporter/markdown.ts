export type MarkdownOptions = {
  /** raw 민감 정보를 포함할지 여부. 기본 false. */
  includeRaw?: boolean;
  /** 헤더에 raw 토큰을 절대 포함하지 않음 (강제). */
  enforceMasking?: boolean;
  /**
   * Timeline에서 noteworthy 이벤트만 표시할지. 기본 true.
   * false면 모든 AuthEvent를 timeline에 노출.
   */
  compactTimeline?: boolean;
};

